package logika;

public class Polozaj {
	private String ime;
	private int x;
	private int y;
	
	public Polozaj(String ime) {
		this.ime = ime;
		this.x = 0;
		this.y = 0;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public String getIme() {
		return ime;
	}

	
	
}
